package day08;

public class MathTest {
	public static void main(String[] args) {
		System.out.println(Math.PI);
		System.out.println(Math.ceil(3.14)); // 올림(첫째자리에서)double
		System.out.println(Math.floor(3.14));// 버림double
		System.out.println(Math.round(3.14));// 반올림 long
		System.out.println(Math.sqrt(9));//루트
		System.out.println(Math.pow(2, 10));//2의10승
		System.out.println(Math.random());//1보다 작은 16자리 실수 랜덤 생성.
		
	}
}
